// 1. Faça um programa que mostre a contagem de 1 a 10.
// 1. A program that counts from 1 to 10.

for (let i=1; i<=10; i++) {
    console.log(i);
}


// O for loop é útil para este código que conta de 1 a 10 porque ele nos permite iterar um número específico de vezes, neste caso, de 1 a 10.
// A instrução de inicialização do loop inicializa a variável de loop i para 1, e a condição do loop verifica se i é menor ou igual a 10.
// Durante cada iteração, a declaração do loop incrementa i em 1 e o método console.log() é chamado, o que imprime o valor atual de i no console.
// O for loop for nos permite escrever um código conciso e claro que conta de 1 a 10 sem ter que escrever declarações individuais console.log() para cada número. 
// Em vez disso, podemos usar um loop para automatizar o processo, o que torna mais fácil manter e modificar o código.

// The for loop is useful for this code because it allows us to iterate a specific number of times, in this case, from 1 to 10.
// The loop's initialization statement initializes the loop variable i to 1, and the loop condition checks whether i is less than or equal to 10.
// During each iteration, the loop statement increments i by 1, and the console.log() method is called, which prints the current value of i to the console.
// The for loop allows us to write a concise and clear code that counts numbers from 1 to 10 without having to write individual console.log() statements for each number. 
// Instead, we can use a loop to automate the process, which makes it easier to maintain and modify the code.