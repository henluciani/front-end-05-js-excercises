// 2. Faça um programa que mostre a contagem de 10 a 1.
// 2. A program that counts from 10 to 1

for (let i=10; i>=1; i--) {
    console.log(i);
}

// A instrução de inicialização do loop inicializa a variável de loop i para 10 e a condição do loop verifica se i é maior ou igual a 1.
// Durante cada iteração, a declaração do loop decrementa i em 1 e o método console.log() é chamado, o que imprime o valor atual de i no console.
// Este caso difere do caso anterior porque o loop está contando de 10 até 1, enquanto o anterior contou de 1 até 10. 
// A condição e a instrução de inicialização do loop também são diferentes para acomodar essa diferença.


// The loop's initialization statement initializes the loop variable i to 10, and the loop condition checks whether i is greater than or equal to 1.
// During each iteration, the loop statement decrements i by 1, and the console.log() method is called, which prints the current value of i to the console.
// This case differs from the previous one because the loop is counting down from 10 to 1, whereas the previous one counted up from 1 to 10. 
// The loop's condition and initialization statement are also different to accommodate this difference.